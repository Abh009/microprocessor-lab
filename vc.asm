 .MODEL SMALL
 
  .STACK 100H 
          
  .DATA
  STRING DB "ABCDE$"
  VOWEL DB ?
  MSG1 DB 10,13,'ENTER ANY STRING : $'
  MSG3 DB 10,13,'NO OF VOWELS : $' 
  P1 LABEL BYTE
  M12 DB 0FFH 
  L1 DB ?
  X DB ?
   X1 DB ?
    X2 DB ?
  M1 DB 0FFH  DUP ('$')
 
DISPLAY MACRO MSG
    MOV AH,9
    LEA DX,MSG
    INT 21H
ENDM  
  .CODE
  MOV AX, @DATA
  MOV DS, AX
  DISPLAY MSG1
       
LEA DX,P1
MOV AH,0AH    
INT 21H

DISPLAY MSG3
       
LEA SI,M1
  MOV BL, 48
   
 DEC SI
  BACK: MOV AL, [SI]


  CMP AL,'$'
  JZ FINAL
  CMP AL,'A'
  JZ COUNT   
  CMP AL,'E'
  JZ COUNT   
  CMP AL,'I'
  JZ COUNT   
  CMP AL,'O'
  JZ COUNT   
  CMP AL,'U'
  JZ COUNT
  CMP AL,'a'
  JZ COUNT   
  CMP AL,'e'
  JZ COUNT   
  CMP AL,'i'
  JZ COUNT   
  CMP AL,'o'
  JZ COUNT   
  CMP AL,'u'
  JZ COUNT   
  INC SI
  JMP BACK 
  COUNT: INC BL
  MOV VOWEL, BL
  INC SI
  JMP BACK
  
  FINAL: 
  MOV DL,BL
  MOV AH, 02H
  INT 21H

  MOV AH, 4CH
  INT 21H


  END  
